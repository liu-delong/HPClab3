void loop_expansion(int n,int **A,int **B,int **C)
{
    for(int i=0;i<n;i+=4)
    {
        for(int j=0;j<n;j+=4)
        {
            C[i+0][j+0]=0;C[i+0][j+1]=0;C[i+0][j+2]=0;C[i+0][j+3]=0;
            C[i+1][j+0]=0;C[i+1][j+1]=0;C[i+1][j+2]=0;C[i+1][j+3]=0;
            C[i+2][j+0]=0;C[i+2][j+1]=0;C[i+2][j+2]=0;C[i+2][j+3]=0;
            C[i+3][j+0]=0;C[i+3][j+1]=0;C[i+3][j+2]=0;C[i+3][j+3]=0;
            for(int k=0;k<n;k+=4)
            {
                C[i+0][j+0]+=A[i+0][k+0]*B[k+0][j+0];
                C[i+0][j+1]+=A[i+0][k+0]*B[k+0][j+1];
                C[i+0][j+2]+=A[i+0][k+0]*B[k+0][j+2];
                C[i+0][j+3]+=A[i+0][k+0]*B[k+0][j+3];

                C[i+1][j+0]+=A[i+1][k+0]*B[k+0][j+0];
                C[i+1][j+1]+=A[i+1][k+0]*B[k+0][j+1];
                C[i+1][j+2]+=A[i+1][k+0]*B[k+0][j+2];
                C[i+1][j+3]+=A[i+1][k+0]*B[k+0][j+3];
                
                C[i+2][j+0]+=A[i+2][k+0]*B[k+0][j+0];
                C[i+2][j+1]+=A[i+2][k+0]*B[k+0][j+1];
                C[i+2][j+2]+=A[i+2][k+0]*B[k+0][j+2];
                C[i+2][j+3]+=A[i+2][k+0]*B[k+0][j+3];

                C[i+3][j+0]+=A[i+3][k+0]*B[k+0][j+0];
                C[i+3][j+1]+=A[i+3][k+0]*B[k+0][j+1];
                C[i+3][j+2]+=A[i+3][k+0]*B[k+0][j+2];
                C[i+3][j+3]+=A[i+3][k+0]*B[k+0][j+3];


            }
        }
        
    }
}